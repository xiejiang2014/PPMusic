namespace PPMusic.Player.Enum
{
    /// <summary>
    /// 调号
    /// </summary>
    public enum KeySignature
    {
        //Major
        CFlatMajor, //bC调 - Cb大调， Ab小调

        GFlatMajor,  //bG调 - Gb大调，Eb小调
        DFlatMajor,  //bD调 - Db大调，Bb小调
        AFlatMajor,  //bA调 - Ab大调，f小调
        EFlatMajor,  //bE调 - Eb大调，c小调
        BFlatMajor,  //bB调 - Bb大调，g小调
        FMajor,      //F调 - F大调，d小调
        CMajor,      //C调 - C大调，a小调
        GMajor,      //G调 - G大调，e小调
        DMajor,      //D调 - D大调，b小调
        AMajor,      //A调 - A大调，f#小调
        EMajor,      //E调 - E大调，c#小调
        BMajor,      //B调 - B大调，g#小调
        FSharpMajor, //#F调 - F#大调，d#小调
        CSharpMajor, //#C调 - C#大调，a#小调

        //Minor
        AFlatMinor,
        EFlatMinor,
        BFlatMinor,
        FMinor,
        CMinor,
        GMinor,
        DMinor,
        AMinor,
        EMinor,
        BMinor,
        FSharpMinor,
        CSharpMinor,
        GSharpMinor,
        DSharpMinor,
        ASharpMinor,
    }
}